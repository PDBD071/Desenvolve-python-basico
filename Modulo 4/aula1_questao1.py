while True:
    # Leia o valor de x
    x = int(input("Digite o valor de x: "))

    # Verifique se x é maior que 5
    if x > 5:
        # Se sim, imprima "maior que 5"
        print("maior que 5")
    else:
        # Se não, imprima "fim" e saia do loop
        print("fim")
        break



